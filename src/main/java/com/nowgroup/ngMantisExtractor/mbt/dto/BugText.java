package com.nowgroup.ngMantisExtractor.mbt.dto;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author https://github.com/diego-torres
 *
 */
@Entity
@Table(name = "mantis_bug_text_table")
public class BugText implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String description;
	private String stepsToReproduce;
	private String additionalInformation;

	/**
	 * @return the id
	 */
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the description
	 */
	@Column
	public String getDescription() {
		return description;
	}

	/**
	 * @param description
	 *            the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return the stepsToReproduce
	 */
	@Column(name="steps_to_reproduce")
	public String getStepsToReproduce() {
		return stepsToReproduce;
	}

	/**
	 * @param stepsToReproduce
	 *            the stepsToReproduce to set
	 */
	public void setStepsToReproduce(String stepsToReproduce) {
		this.stepsToReproduce = stepsToReproduce;
	}

	/**
	 * @return the additionalInformation
	 */
	@Column(name="additional_information")
	public String getAdditionalInformation() {
		return additionalInformation;
	}

	/**
	 * @param additionalInformation
	 *            the additionalInformation to set
	 */
	public void setAdditionalInformation(String additionalInformation) {
		this.additionalInformation = additionalInformation;
	}

}
